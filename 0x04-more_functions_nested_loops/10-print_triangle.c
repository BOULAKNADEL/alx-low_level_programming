#include "main.h"

/**
 * print_triangle - print  a triangle
 *
 * @size: the size of the triangle
 *
 * Return: void
 */


void print_triangle(int size)
{
	int i, j;

	if (size <= 0)
		_putchar('\n');
	for (i = 0; i < j; i++)
	{
		for (j = 0; j < size; j++)
		{
			_putchar('#');
		}
		_putchar(' ');
	}

}
